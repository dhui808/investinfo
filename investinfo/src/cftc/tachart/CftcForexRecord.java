package cftc.tachart;

public class CftcForexRecord {

	private String releaseDate;
	private String instrument;
	private Integer dealerLong;
	private Integer dealerShort;
	private Integer assetMgrLong;
	private Integer assetMgrShort;
	private Integer levMoneyLong;
	private Integer levMoneyShort;
	private Integer otherLong;
	private Integer otherShort;
	
	public String getReleaseDate() {
		return releaseDate;
	}
	public void setReleaseDate(String releaseDate) {
		this.releaseDate = releaseDate;
	}
	public String getInstrument() {
		return instrument;
	}
	public void setInstrument(String instrument) {
		this.instrument = instrument;
	}
	public Integer getDealerLong() {
		return dealerLong;
	}
	public void setDealerLong(Integer dealerLong) {
		this.dealerLong = dealerLong;
	}
	public Integer getDealerShort() {
		return dealerShort;
	}
	public void setDealerShort(Integer dealerShort) {
		this.dealerShort = dealerShort;
	}
	public Integer getAssetMgrLong() {
		return assetMgrLong;
	}
	public void setAssetMgrLong(Integer assetMgrLong) {
		this.assetMgrLong = assetMgrLong;
	}
	public Integer getAssetMgrShort() {
		return assetMgrShort;
	}
	public void setAssetMgrShort(Integer assetMgrShort) {
		this.assetMgrShort = assetMgrShort;
	}
	public Integer getLevMoneyLong() {
		return levMoneyLong;
	}
	public void setLevMoneyLong(Integer levMoneyLong) {
		this.levMoneyLong = levMoneyLong;
	}
	public Integer getLevMoneyShort() {
		return levMoneyShort;
	}
	public void setLevMoneyShort(Integer levMoneyShort) {
		this.levMoneyShort = levMoneyShort;
	}
	public Integer getOtherLong() {
		return otherLong;
	}
	public void setOtherLong(Integer otherLong) {
		this.otherLong = otherLong;
	}
	public Integer getOtherShort() {
		return otherShort;
	}
	public void setOtherShort(Integer otherShort) {
		this.otherShort = otherShort;
	}
	
	
}
